/* this fuction creates a score_m array XX-series, which contains pointers to all matrices of the XX matrix series */
score_m **init_matseries()
{
  score_m **matx;
    
  /* the pam and blosum series have each 4 members:
     pam_series [pam]: pam_20, pam_60, pam_120, pam_350; 
     blosum_series [blo]: blosum_30, blosum_50, blosum_62, blosum_80;

     the gonnet_series, which is the default series, has 7 members:
     gonnet_series [gon]: gonnet_40, gonnet_80, gonnet_120, gonnet_160,
                          gonnet_250, gonnet_300, gonnet_350;
  */
  
 
  if(strncasecmp(MAT_SER,"gon",3) == 0)
    {
      matx =(score_m**) calloc((7),sizeof(score_m*));

      matx[0]=&gonnet_40;
      matx[1]=&gonnet_80;
      matx[2]=&gonnet_120;
      matx[3]=&gonnet_160;
      matx[4]=&gonnet_250;
      matx[5]=&gonnet_300;
      matx[6]=&gonnet_350;
      

      if(plus() != 0)
	{
	  fprintf(stderr,"init_matseries: gon_p matrices are not available");
	  usage(0);
	  exit(20);
	}
    }
  else if(strncasecmp(MAT_SER,"blo",3) == 0)
    {
      matx =(score_m**) calloc((4),sizeof(score_m*));

      matx[0]=&blosum_30;
      matx[1]=&blosum_50;
      matx[2]=&blosum_62;
      matx[3]=&blosum_80;
    }
  else if(strncasecmp(MAT_SER,"pam",3) == 0)
    {
      matx =(score_m**) calloc((4),sizeof(score_m*));

      matx[0]=&pam_20;
      matx[1]=&pam_60;
      matx[2]=&pam_120;
      matx[3]=&pam_350;
    }
  else
  /*  if((strncasecmp(MAT_SER,"gon",3)!= 0) && (strncasecmp(MAT_SER,"blo",3)!= 0) && (strncasecmp(MAT_SER,"pam",3)!= 0)) */
    {
      fprintf(stderr,"init_matseries: error in scoring matrix selection, no score matrix selected");
       usage(0);
       exit(21);
    }
  return(matx);
  
}




























































































